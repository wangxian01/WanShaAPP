package com.example.l.wanshaapp.bean;

public class Small {

    private String name;

    private int imageId;

    public Small(String name, int imageId ) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}